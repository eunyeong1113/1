def plus(a,b):
    return a + b
print(plus(5,6))

def minus(a,b):
    return a - b
print(minus(5,6))

def times(a,b):
    return a * b
print(times(5,6))

def division(a,b):
    return a / b
print(division(30,6))

def negation(a):
    return -a
print(negation(6))

def power(a,b):
    return a**b
print(power(5,6))

def remainder(a,b):
    return a%b
print(remainder(14,6))


